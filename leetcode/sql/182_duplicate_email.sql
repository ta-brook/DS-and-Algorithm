-- Write an SQL query to report all the duplicate emails.

SELECT
    email
FROM
    Person
GROUP BY
    email
HAVING
    count(1) > 1


/*
Example 1:

Input: 
Person table:
+----+---------+
| id | email   |
+----+---------+
| 1  | a@b.com |
| 2  | c@d.com |
| 3  | a@b.com |
+----+---------+
Output: 
+---------+
| Email   |
+---------+
| a@b.com |
+---------+
Explanation: a@b.com is repeated two times.
*/